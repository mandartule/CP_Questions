import java.util.*;
 
public class followingTheString {
 
    public static void main(String args[]) {
 
        Scanner sc = new Scanner(System.in);
 
        int n = sc.nextInt();
 
        for (int i = 0; i < n; i++) {
            
            
            int m = sc.nextInt();
            int arr[] = new int[m];

            for(int j=0; j<m; j++){
                arr[j] = sc.nextInt();
            }
            
            
        }
 
    }
}